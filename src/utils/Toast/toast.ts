import { toast } from 'react-toastify';

export const errorMessage = (mensage: string) =>
  toast.error(mensage, {
    position: 'top-right',
    autoClose: 3000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
  });

  
